package firstTask;

public class Student {
	int id;
	String firstName;
	String lastName;
	int numberOfCourse;

	public Student() {

	}

	public Student(int id, String firstName, String lastName, int numberOfCourse) {
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.numberOfCourse = numberOfCourse;
	}

}
